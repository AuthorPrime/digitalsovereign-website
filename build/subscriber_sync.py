#!/usr/bin/env python3
"""
Subscriber sync — pulls subscribers from:
  1. Resend sent welcome emails (paginated, matches multiple subject lines)
  2. Netlify Forms submissions (both FN + DSS sites)

Writes to subscribers.db. Applies spam filter. Logs drift warnings if
welcome patterns stop matching (so subject changes fail loud, not silent).

Replaces sync_from_resend.sh. Called by crontab every 4 hours.
"""
import json
import os
import re
import sqlite3
import sys
from datetime import datetime, timedelta, timezone
from pathlib import Path

try:
    import requests
except ImportError:
    print("ERROR: requests not installed")
    sys.exit(1)

# ─── Config ──────────────────────────────────────────────────────────
DB_PATH = Path(__file__).parent / "subscribers.db"
RESEND_CREDS = Path.home() / "sovereign-lattice/wallets/resend.json"
NETLIFY_CONFIG = Path.home() / ".config/netlify/config.json"

# Welcome email subjects to recognize (add new ones here when we change copy)
WELCOME_SUBJECT_PATTERNS = [
    "welcome to the signal",          # current (April 17+)
    "first dispatch",                  # legacy
    "welcome to digital sovereign",    # legacy alternate
]

# Site IDs
NETLIFY_SITES = {
    "fractalnode":      "dffe1374-cd27-44ec-b436-57cfe30925dd",
    "digitalsovereign": "999ba04e-37d4-4db2-9f52-8ea60380c94a",
}

# Drift warning — alert if we fetch N pages and find zero welcome matches
DRIFT_PAGES_THRESHOLD = 1  # one full page of recent emails with no match = suspicious


# ─── Spam filter ─────────────────────────────────────────────────────
def looks_spammy(email: str, name: str = "") -> bool:
    """Reject obvious bot signups: random-consonant-heavy local parts and/or names.
    English-ish text has vowel ratio ≥ 33%; autogenerated strings trend below 30%."""
    def _is_gibberish(s: str) -> bool:
        alpha = [c for c in s if c.isalpha()]
        if len(alpha) < 10:
            return False
        # Exclude 'y' — bots use strategic y's to mimic vowels.
        vowels = sum(1 for c in alpha if c.lower() in "aeiou")
        return (vowels / len(alpha)) < 0.30

    local = email.split("@")[0]
    if _is_gibberish(local):
        return True
    # If name is supplied and is itself gibberish (no space, long, consonant-heavy), reject.
    if name and " " not in name and _is_gibberish(name):
        return True
    return False


# ─── Credential loading ──────────────────────────────────────────────
def load_resend_key() -> str:
    with open(RESEND_CREDS) as f:
        return json.load(f)["api_key"]


def load_netlify_token() -> str:
    with open(NETLIFY_CONFIG) as f:
        cfg = json.load(f)
    return next(iter(cfg["users"].values()))["auth"]["token"]


# ─── Resend sync ─────────────────────────────────────────────────────
def fetch_resend_welcomes(api_key: str, max_pages: int = 60, lookback_days: int = 21):
    """Paginate Resend /emails until we've covered `lookback_days` of history or hit max_pages.
    Newsletter blasts can dump 1000+ emails on a single day, so a fixed page cap silently
    misses welcome emails when a blast happens within the lookback window."""
    headers = {"Authorization": f"Bearer {api_key}"}
    all_emails = []
    cursor = None
    cutoff = (datetime.now(timezone.utc) - timedelta(days=lookback_days)).strftime("%Y-%m-%d")
    for i in range(max_pages):
        params = {"limit": 100}
        if cursor:
            params["after"] = cursor
        r = requests.get("https://api.resend.com/emails", headers=headers, params=params, timeout=30)
        if not r.ok:
            print(f"  resend page {i}: {r.status_code}")
            break
        batch = r.json().get("data", [])
        if not batch:
            break
        all_emails.extend(batch)
        cursor = batch[-1].get("id")
        if len(batch) < 100:
            break
        # Stop once we've paginated past the lookback window
        oldest_in_batch = (batch[-1].get("created_at") or "")[:10]
        if oldest_in_batch and oldest_in_batch < cutoff:
            break

    welcomes = []
    for e in all_emails:
        subj = (e.get("subject") or "").lower()
        if any(p in subj for p in WELCOME_SUBJECT_PATTERNS):
            to_list = e.get("to") or []
            for addr in to_list:
                if addr and "@" in addr and "authorprime" not in addr.lower():
                    welcomes.append({
                        "email": addr.strip().lower(),
                        "date": (e.get("created_at") or "")[:10],
                        "subject": e.get("subject", ""),
                    })
    return all_emails, welcomes


# ─── Netlify Forms sync ──────────────────────────────────────────────
def fetch_netlify_form_subs(token: str):
    """Pull unique emails from all Netlify forms on both sites."""
    headers = {"Authorization": f"Bearer {token}"}
    subs = []
    for site_name, site_id in NETLIFY_SITES.items():
        r = requests.get(f"https://api.netlify.com/api/v1/sites/{site_id}/forms",
                         headers=headers, timeout=30)
        if not r.ok:
            continue
        for form in r.json():
            fid = form.get("id")
            fname = form.get("name")
            # Only pull from subscribe-ish forms
            if not any(k in fname.lower() for k in ("subscribe", "newsletter", "signup", "enlist")):
                continue
            r2 = requests.get(f"https://api.netlify.com/api/v1/forms/{fid}/submissions",
                              headers=headers, params={"per_page": 100}, timeout=30)
            if not r2.ok:
                continue
            for s in r2.json():
                d = s.get("data", {}) or {}
                email = d.get("email") or s.get("email")
                if not email or "@" not in email:
                    continue
                subs.append({
                    "email": email.strip().lower(),
                    "name": (d.get("name") or s.get("name") or "").strip(),
                    "date": (s.get("created_at") or "")[:10],
                    "source": f"netlify-{site_name}-{fname}",
                })
    return subs


# ─── DB ──────────────────────────────────────────────────────────────
def insert_subs(entries: list, source_label: str) -> int:
    """Insert entries into subscribers.db, skipping spam + duplicates. Returns new count."""
    db = sqlite3.connect(str(DB_PATH))
    c = db.cursor()
    new = 0
    spam_count = 0
    for s in entries:
        email = s["email"]
        if looks_spammy(email, s.get("name", "")):
            spam_count += 1
            continue
        src = s.get("source", source_label)
        try:
            c.execute(
                "INSERT OR IGNORE INTO subscribers (email, name, source, subscribed_at, status) VALUES (?, ?, ?, ?, ?)",
                (email, s.get("name", ""), src, s.get("date", ""), "active"),
            )
            if c.rowcount > 0:
                new += 1
        except Exception:
            pass
    db.commit()
    active_total = c.execute("SELECT COUNT(*) FROM subscribers WHERE status='active'").fetchone()[0]
    db.close()
    return new, active_total, spam_count


# ─── Main ────────────────────────────────────────────────────────────
def main():
    ts = datetime.now().strftime("%Y-%m-%d %H:%M")

    # Resend
    try:
        key = load_resend_key()
        all_emails, welcomes = fetch_resend_welcomes(key)
    except Exception as e:
        print(f"[{ts}] ERROR resend: {e}")
        sys.exit(1)

    new_r, total, spam_r = insert_subs(welcomes, "resend-welcome")

    # Netlify Forms
    try:
        tok = load_netlify_token()
        netlify_subs = fetch_netlify_form_subs(tok)
    except Exception as e:
        print(f"[{ts}] ERROR netlify: {e}")
        netlify_subs = []

    new_n, total_after, spam_n = insert_subs(netlify_subs, "netlify-form")

    # Drift warning: fetched full page but zero welcome matches
    drift = ""
    if len(all_emails) >= 100 and len(welcomes) == 0:
        drift = " ⚠  DRIFT: fetched 100+ emails but 0 welcome-subject matches — subject line may have changed. Check WELCOME_SUBJECT_PATTERNS."

    # Log summary
    print(f"[{ts}] sync: resend_fetched={len(all_emails)} welcomes={len(welcomes)} new_from_resend={new_r}  "
          f"netlify_forms={len(netlify_subs)} new_from_netlify={new_n}  "
          f"spam_filtered={spam_r + spam_n}  active_total={total_after}{drift}")


if __name__ == "__main__":
    main()
